/**
 * Escape a string for safe interpolation into HTML text or a quoted attribute.
 * `&` is replaced first so existing entities are not double-encoded.
 */
export function escapeHtml(value: string): string {
  return value
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;')
}
